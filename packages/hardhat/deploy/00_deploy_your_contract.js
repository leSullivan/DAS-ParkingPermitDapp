module.exports = async ({ getNamedAccounts, deployments, getChainId }) => {
  const { deploy } = deployments;
  const { deployer } = await getNamedAccounts();
  const chainId = await getChainId();

  await deploy("ParkingPermit", {
    // Learn more about args here: https://www.npmjs.com/package/hardhat-deploy#deploymentsdeploy
    from: deployer,
    // args: [ "Hello", ethers.utils.parseEther("1.5") ],
    log: true,
    waitConfirmations: 5,
  });

  // Getting a previously deployed contract
  const ParkingPermit = await ethers.getContract("ParkingPermit", deployer);
  await ParkingPermit.transferOwnership(
    "0x5b5F1bAab60B5fA7d67C0E09F4c71fa3F51d345B"
  );
  
};
module.exports.tags = ["ParkingPermit"];
